fn seat_id(pass: &String) -> u32 {
    let mut n = 0;

    for c in pass.bytes() {
        n *= 2;
        n += match c {
            b'B' => 1,
            b'R' => 1,
            b'L' => 0,
            b'F' => 0,
            _ => unreachable!(),
        };
    }

    n
}

pub fn pt1(passes: &[String]) -> u32 {
    passes.iter().map(seat_id).max().unwrap()
}

pub fn pt2(passes: &[String]) -> Option<u32> {
    let mut found = [false; 1024];

    for n in passes.iter().map(seat_id) {
        found[n as usize] = true;
    }

    for i in 1..found.len() - 1 {
        if !found[i] && found[i - 1] && found[i + 1] {
            return Some(i as u32);
        }
    }
    None
}

