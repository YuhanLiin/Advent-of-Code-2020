use regex::Regex;

fn process(input: &[String], predicate: impl Fn(&[u8], u8, usize, usize) -> bool) -> usize {
    let regex = Regex::new(r"(\d+)-(\d+) ([a-z]): ([a-z]+)").unwrap();
    let mut valid = 0;

    for line in input {
        let caps = regex.captures(line).unwrap();
        let a: usize = caps.get(1).unwrap().as_str().parse().unwrap();
        let b: usize = caps.get(2).unwrap().as_str().parse().unwrap();
        let ch = caps.get(3).unwrap().as_str().as_bytes()[0];
        let pw = caps.get(4).unwrap().as_str().as_bytes();

        if predicate(pw, ch, a, b) {
            valid += 1;
        }
    }

    valid
}

pub fn pt1(input: &[String]) -> usize {
    process(input, |pw, ch, min, max| {
        let count = pw.iter().filter(|&&c| c == ch).count();
        count >= min && count <= max
    })
}

pub fn pt2(input: &[String]) -> usize {
    process(input, |pw, ch, a, b| (pw[a - 1] == ch) ^ (pw[b - 1] == ch))
}

